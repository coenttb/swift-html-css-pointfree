import CSSTypes
import PointFreeHTML

extension HTML {
    @discardableResult
    public func mozOrient(
        _ mozOrient: CSSPropertyTypes.MozOrient?,
        media: CSSAtRuleTypes.Media? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(mozOrient, media: media, pre: pre, pseudo: pseudo)
    }
}
