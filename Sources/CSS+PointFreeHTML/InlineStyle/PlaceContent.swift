import CSSTypes
import PointFreeHTML

extension HTML {
    @discardableResult
    public func placeContent(
        _ placeContent: CSSPropertyTypes.PlaceContent?,
        media: CSSAtRuleTypes.Media? = nil,
        selector: Selector? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(placeContent, media: media, selector: selector, pseudo: pseudo)
    }
}
