import CSSTypes
import PointFreeHTML

extension HTML {
    @discardableResult
    public func lineBreak(
        _ lineBreak: CSSPropertyTypes.LineBreak?,
        media: CSSAtRuleTypes.Media? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(lineBreak, media: media, pre: pre, pseudo: pseudo)
    }
}
