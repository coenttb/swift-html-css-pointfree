import CSSTypes
import PointFreeHTML

extension HTML {
    @discardableResult
    public func textOrientation(
        _ textOrientation: CSSPropertyTypes.TextOrientation?,
        media: CSSAtRuleTypes.Media? = nil,
        selector: Selector? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(textOrientation, media: media, selector: selector, pseudo: pseudo)
    }
}
