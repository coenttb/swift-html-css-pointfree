import CSSTypes
import PointFreeHTML

extension HTML {
    @discardableResult
    public func filter(
        _ filter: CSSPropertyTypes.Filter?,
        media: CSSAtRuleTypes.Media? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(filter, media: media, pre: pre, pseudo: pseudo)
    }
}
