import CSSTypes
import PointFreeHTML

extension HTML {
    @discardableResult
    public func stopOpacity(
        _ stopOpacity: CSSPropertyTypes.StopOpacity?,
        media: CSSAtRuleTypes.Media? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(stopOpacity, media: media, pre: pre, pseudo: pseudo)
    }
}
