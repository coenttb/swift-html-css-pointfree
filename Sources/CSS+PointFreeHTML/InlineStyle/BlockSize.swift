import CSSTypes
import PointFreeHTML

extension HTML {
    @discardableResult
    public func blockSize(
        _ blockSize: CSSPropertyTypes.BlockSize?,
        media: CSSAtRuleTypes.Media? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(blockSize, media: media, pre: pre, pseudo: pseudo)
    }
}
