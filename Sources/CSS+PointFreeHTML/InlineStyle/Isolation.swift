import CSSTypes
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func isolation(
        _ isolation: CSSPropertyTypes.Isolation?,
        media: CSSAtRuleTypes.Media? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(isolation, media: media, pre: pre, pseudo: pseudo)
    }
}
