import CSSTypes
import PointFreeHTML

extension HTML {
    @discardableResult
    public func stroke(
        _ stroke: CSSPropertyTypes.Stroke?,
        media: CSSAtRuleTypes.Media? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(stroke, media: media, pre: pre, pseudo: pseudo)
    }
}
