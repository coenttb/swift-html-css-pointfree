import CSSTypes
import PointFreeHTML

extension HTML {
    @discardableResult
    public func minWidth(
        _ minWidth: CSSPropertyTypes.MinWidth?,
        media: CSSAtRuleTypes.Media? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(minWidth, media: media, pre: pre, pseudo: pseudo)
    }
}
