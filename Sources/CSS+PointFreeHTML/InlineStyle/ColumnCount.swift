import CSSTypes
import PointFreeHTML

extension HTML {
    @discardableResult
    public func columnCount(
        _ columnCount: CSSPropertyTypes.ColumnCount?,
        media: CSSAtRuleTypes.Media? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(columnCount, media: media, pre: pre, pseudo: pseudo)
    }
}
