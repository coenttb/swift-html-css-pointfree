import CSSTypes
import PointFreeHTML

extension HTML {
    @discardableResult
    public func overlay(
        _ overlay: CSSPropertyTypes.Overlay?,
        media: CSSAtRuleTypes.Media? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(overlay, media: media, pre: pre, pseudo: pseudo)
    }
}
