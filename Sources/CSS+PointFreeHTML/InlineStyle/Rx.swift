import CSSTypes
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func rx(
        _ rx: CSSPropertyTypes.Rx?,
        media: CSSAtRuleTypes.Media? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(rx, media: media, pre: pre, pseudo: pseudo)
    }
}
