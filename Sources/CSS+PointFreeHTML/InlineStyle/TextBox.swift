import CSSTypes
import PointFreeHTML

extension HTML {
    @discardableResult
    public func textBox(
        _ textBox: CSSPropertyTypes.TextBox?,
        media: CSSAtRuleTypes.Media? = nil,
        selector: Selector? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(textBox, media: media, selector: selector, pseudo: pseudo)
    }
}
