import CSSTypes
import PointFreeHTML

extension HTML {
    @discardableResult
    public func display(
        _ display: CSSPropertyTypes.Display?,
        media: CSSAtRuleTypes.Media? = nil,
        selector: Selector? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(display, media: media, selector: selector, pseudo: pseudo)
    }
}
