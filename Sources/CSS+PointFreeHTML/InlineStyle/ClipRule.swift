import CSSTypes
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func clipRule(
        _ clipRule: CSSPropertyTypes.ClipRule?,
        media: CSSAtRuleTypes.Media? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(clipRule, media: media, pre: pre, pseudo: pseudo)
    }
}
