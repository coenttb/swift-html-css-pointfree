import CSSTypes
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func objectFit(
        _ objectFit: CSSPropertyTypes.ObjectFit?,
        media: CSSAtRuleTypes.Media? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(objectFit, media: media, pre: pre, pseudo: pseudo)
    }
}
