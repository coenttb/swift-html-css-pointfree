import CSSTypes
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func outline(
        _ outline: CSSPropertyTypes.Outline?,
        media: CSSAtRuleTypes.Media? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(outline, media: media, pre: pre, pseudo: pseudo)
    }
}
