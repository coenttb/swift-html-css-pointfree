import CSSTypes
import PointFreeHTML

extension HTML {
    @discardableResult
    public func opacity(
        _ opacity: CSSPropertyTypes.Opacity?,
        media: CSSAtRuleTypes.Media? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(opacity, media: media, pre: pre, pseudo: pseudo)
    }
}
