import HTMLAttributeTypes
import PointFreeHTML

extension HTML {

    /// Sets the disableremoteplayback attribute on an element
    @discardableResult
    package func download(
        _ value: Download?
    ) -> _HTMLAttributes<Self> {
        self.attribute(Download.attribute, value?.description)
    }
}
