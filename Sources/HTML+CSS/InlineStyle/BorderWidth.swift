import CSSPropertyTypes
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func borderWidth(
        _ borderWidth: CSSPropertyTypes.BorderWidth?,
        media mediaQuery: MediaQuery? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(borderWidth, media: mediaQuery, pre: pre, pseudo: pseudo)
    }
}
