import CSSTypes
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func tableLayout(
        _ tableLayout: CSSPropertyTypes.TableLayout?,
        media: CSSAtRuleTypes.Media? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(tableLayout, media: media, pre: pre, pseudo: pseudo)
    }
}
