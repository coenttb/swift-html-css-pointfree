import CSSTypes
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func maskOrigin(
        _ maskOrigin: CSSPropertyTypes.MaskOrigin?,
        media: CSSAtRuleTypes.Media? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(maskOrigin, media: media, pre: pre, pseudo: pseudo)
    }
}
