import CSSTypes
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func resize(
        _ resize: CSSPropertyTypes.Resize?,
        media: CSSAtRuleTypes.Media? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(resize, media: media, pre: pre, pseudo: pseudo)
    }
}
