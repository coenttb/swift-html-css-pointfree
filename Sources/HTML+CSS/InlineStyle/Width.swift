import CSSPropertyTypes
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func width(
        _ width: CSSPropertyTypes.Width?,
        media mediaQuery: MediaQuery? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(width, media: mediaQuery, pre: pre, pseudo: pseudo)
    }
}
