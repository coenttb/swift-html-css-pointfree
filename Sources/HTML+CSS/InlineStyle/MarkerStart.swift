import CSSTypes
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func markerStart(
        _ markerStart: CSSPropertyTypes.MarkerStart?,
        media: CSSAtRuleTypes.Media? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(markerStart, media: media, pre: pre, pseudo: pseudo)
    }
}
