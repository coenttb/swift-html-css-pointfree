import CSSTypes
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func padding(
        _ padding: CSSPropertyTypes.Padding?,
        media: CSSAtRuleTypes.Media? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(padding, media: media, pre: pre, pseudo: pseudo)
    }
}
