import CSSPropertyTypes
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func flex(
        _ flex: CSSPropertyTypes.Flex?,
        media mediaQuery: MediaQuery? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(flex, media: mediaQuery, pre: pre, pseudo: pseudo)
    }
}
