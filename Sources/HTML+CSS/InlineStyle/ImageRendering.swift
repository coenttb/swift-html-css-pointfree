import CSSPropertyTypes
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func imageRendering(
        _ imageRendering: CSSPropertyTypes.ImageRendering?,
        media mediaQuery: MediaQuery? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(imageRendering, media: mediaQuery, pre: pre, pseudo: pseudo)
    }
}
