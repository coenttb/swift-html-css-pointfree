import CSSPropertyTypes
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func marker(
        _ marker: CSSPropertyTypes.Marker?,
        media mediaQuery: MediaQuery? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(marker, media: mediaQuery, pre: pre, pseudo: pseudo)
    }
}
