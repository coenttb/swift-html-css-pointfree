import CSSPropertyTypes
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func offset(
        _ offset: CSSPropertyTypes.Offset?,
        media mediaQuery: MediaQuery? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(offset, media: mediaQuery, pre: pre, pseudo: pseudo)
    }
}
