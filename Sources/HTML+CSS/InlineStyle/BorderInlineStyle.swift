import CSSPropertyTypes
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func borderInlineStyle(
        _ borderInlineStyle: CSSPropertyTypes.BorderInlineStyle?,
        media mediaQuery: MediaQuery? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(borderInlineStyle, media: mediaQuery, pre: pre, pseudo: pseudo)
    }
}
