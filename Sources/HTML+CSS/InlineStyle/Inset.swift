import CSSTypes
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func inset(
        _ inset: CSSPropertyTypes.Inset?,
        media: CSSAtRuleTypes.Media? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(inset, media: media, pre: pre, pseudo: pseudo)
    }
}
