import CSSPropertyTypes
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func orphans(
        _ orphans: CSSPropertyTypes.Orphans?,
        media mediaQuery: MediaQuery? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(orphans, media: mediaQuery, pre: pre, pseudo: pseudo)
    }
}
