import CSSPropertyTypes
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func ry(
        _ ry: CSSPropertyTypes.Ry?,
        media mediaQuery: MediaQuery? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(ry, media: mediaQuery, pre: pre, pseudo: pseudo)
    }
}
