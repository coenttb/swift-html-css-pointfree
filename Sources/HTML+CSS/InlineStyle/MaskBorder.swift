import CSSTypes
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func maskBorder(
        _ maskBorder: CSSPropertyTypes.MaskBorder?,
        media: CSSAtRuleTypes.Media? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(maskBorder, media: media, pre: pre, pseudo: pseudo)
    }
}
