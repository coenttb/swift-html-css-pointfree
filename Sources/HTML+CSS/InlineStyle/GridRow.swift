import CSSPropertyTypes
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func gridRow(
        _ gridRow: CSSPropertyTypes.GridRow?,
        media mediaQuery: MediaQuery? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(gridRow, media: mediaQuery, pre: pre, pseudo: pseudo)
    }
}
