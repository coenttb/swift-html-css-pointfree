import CSSPropertyTypes
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func fontWeight(
        _ fontWeight: CSSPropertyTypes.FontWeight?,
        media mediaQuery: MediaQuery? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(fontWeight, media: mediaQuery, pre: pre, pseudo: pseudo)
    }
}
