import CSSTypes
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func borderTop(
        _ borderTop: CSSPropertyTypes.BorderTop?,
        media: CSSAtRuleTypes.Media? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(borderTop, media: media, pre: pre, pseudo: pseudo)
    }
}
