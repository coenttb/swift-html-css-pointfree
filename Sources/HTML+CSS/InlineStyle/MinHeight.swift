import CSSPropertyTypes
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func minHeight(
        _ minHeight: CSSPropertyTypes.MinHeight?,
        media mediaQuery: MediaQuery? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(minHeight, media: mediaQuery, pre: pre, pseudo: pseudo)
    }
}
