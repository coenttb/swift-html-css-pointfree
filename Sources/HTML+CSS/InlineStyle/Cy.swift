import CSSPropertyTypes
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func cy(
        _ cy: CSSPropertyTypes.Cy?,
        media mediaQuery: MediaQuery? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(cy, media: mediaQuery, pre: pre, pseudo: pseudo)
    }
}
