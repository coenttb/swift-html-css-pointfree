import CSSPropertyTypes
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func quotes(
        _ quotes: CSSPropertyTypes.Quotes?,
        media mediaQuery: MediaQuery? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(quotes, media: mediaQuery, pre: pre, pseudo: pseudo)
    }
}
