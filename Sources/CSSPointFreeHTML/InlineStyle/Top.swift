import CSSTypes
import PointFreeHTML

extension HTML {
  @discardableResult
  public func top(
    _ top: CSSPropertyTypes.Top?,
    media: CSSAtRuleTypes.Media? = nil,
    selector: Selector? = nil,
    pseudo: Pseudo? = nil
  ) -> HTMLInlineStyle<Self> {
    self.inlineStyle(top, media: media, selector: selector, pseudo: pseudo)
  }
}
