import CSSTypes
import PointFreeHTML

extension HTML {
  @discardableResult
  public func gap(
    _ gap: CSSPropertyTypes.Gap?,
    media: CSSAtRuleTypes.Media? = nil,
    selector: Selector? = nil,
    pseudo: Pseudo? = nil
  ) -> HTMLInlineStyle<Self> {
    self.inlineStyle(gap, media: media, selector: selector, pseudo: pseudo)
  }
}
