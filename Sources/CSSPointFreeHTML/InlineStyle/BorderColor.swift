import CSSTypes
import PointFreeHTML

extension HTML {
  @discardableResult
  public func borderColor(
    _ borderColor: CSSPropertyTypes.BorderColor?,
    media: CSSAtRuleTypes.Media? = nil,
    selector: Selector? = nil,
    pseudo: Pseudo? = nil
  ) -> HTMLInlineStyle<Self> {
    self.inlineStyle(borderColor, media: media, selector: selector, pseudo: pseudo)
  }
}
