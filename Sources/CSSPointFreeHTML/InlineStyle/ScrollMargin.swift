import CSSTypes
import PointFreeHTML

extension HTML {
    @discardableResult
    public func scrollMargin(
        _ scrollMargin: CSSPropertyTypes.ScrollMargin?,
        media: CSSAtRuleTypes.Media? = nil,
        selector: Selector? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(scrollMargin, media: media, selector: selector, pseudo: pseudo)
    }
}
