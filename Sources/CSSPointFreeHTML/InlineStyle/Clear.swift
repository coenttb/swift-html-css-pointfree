import CSSTypes
import PointFreeHTML

extension HTML {
  @discardableResult
  public func clear(
    _ clear: CSSPropertyTypes.Clear?,
    media: CSSAtRuleTypes.Media? = nil,
    selector: Selector? = nil,
    pseudo: Pseudo? = nil
  ) -> HTMLInlineStyle<Self> {
    self.inlineStyle(clear, media: media, selector: selector, pseudo: pseudo)
  }
}
