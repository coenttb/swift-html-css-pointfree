import CSSTypes
import PointFreeHTML

extension HTML {
  @discardableResult
  public func tabSize(
    _ tabSize: CSSPropertyTypes.TabSize?,
    media: CSSAtRuleTypes.Media? = nil,
    selector: Selector? = nil,
    pseudo: Pseudo? = nil
  ) -> HTMLInlineStyle<Self> {
    self.inlineStyle(tabSize, media: media, selector: selector, pseudo: pseudo)
  }
}
