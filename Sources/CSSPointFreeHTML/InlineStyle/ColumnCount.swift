import CSSTypes
import PointFreeHTML

extension HTML {
  @discardableResult
  public func columnCount(
    _ columnCount: CSSPropertyTypes.ColumnCount?,
    media: CSSAtRuleTypes.Media? = nil,
    selector: Selector? = nil,
    pseudo: Pseudo? = nil
  ) -> HTMLInlineStyle<Self> {
    self.inlineStyle(columnCount, media: media, selector: selector, pseudo: pseudo)
  }
}
