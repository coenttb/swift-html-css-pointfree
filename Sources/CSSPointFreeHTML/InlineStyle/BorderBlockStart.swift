import CSSTypes
import PointFreeHTML

extension HTML {
  @discardableResult
  public func borderBlockStart(
    _ borderBlockStart: CSSPropertyTypes.BorderBlockStart?,
    media: CSSAtRuleTypes.Media? = nil,
    selector: Selector? = nil,
    pseudo: Pseudo? = nil
  ) -> HTMLInlineStyle<Self> {
    self.inlineStyle(borderBlockStart, media: media, selector: selector, pseudo: pseudo)
  }
}
