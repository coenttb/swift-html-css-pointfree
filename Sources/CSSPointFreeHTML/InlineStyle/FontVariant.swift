import CSSTypes
import PointFreeHTML

extension HTML {
  @discardableResult
  public func fontVariant(
    _ fontVariant: CSSPropertyTypes.FontVariant?,
    media: CSSAtRuleTypes.Media? = nil,
    selector: Selector? = nil,
    pseudo: Pseudo? = nil
  ) -> HTMLInlineStyle<Self> {
    self.inlineStyle(fontVariant, media: media, selector: selector, pseudo: pseudo)
  }
}
