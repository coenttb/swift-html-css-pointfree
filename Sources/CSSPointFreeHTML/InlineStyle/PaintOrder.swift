import CSSTypes
import PointFreeHTML

extension HTML {
  @discardableResult
  public func paintOrder(
    _ paintOrder: CSSPropertyTypes.PaintOrder?,
    media: CSSAtRuleTypes.Media? = nil,
    selector: Selector? = nil,
    pseudo: Pseudo? = nil
  ) -> HTMLInlineStyle<Self> {
    self.inlineStyle(paintOrder, media: media, selector: selector, pseudo: pseudo)
  }
}
