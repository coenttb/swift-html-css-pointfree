import CSSTypes
import PointFreeHTML

extension HTML {
  @discardableResult
  public func margin(
    _ margin: CSSPropertyTypes.Margin?,
    media: CSSAtRuleTypes.Media? = nil,
    selector: Selector? = nil,
    pseudo: Pseudo? = nil
  ) -> HTMLInlineStyle<Self> {
    self.inlineStyle(margin, media: media, selector: selector, pseudo: pseudo)
  }
}
