import CSSTypes
import PointFreeHTML

extension HTML {
    @discardableResult
    public func transformStyle(
        _ transformStyle: CSSPropertyTypes.TransformStyle?,
        media: CSSAtRuleTypes.Media? = nil,
        selector: Selector? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(transformStyle, media: media, selector: selector, pseudo: pseudo)
    }
}
