import CSSTypes
import PointFreeHTML

extension HTML {
  @discardableResult
  public func zIndex(
    _ zIndex: CSSPropertyTypes.ZIndex?,
    media: CSSAtRuleTypes.Media? = nil,
    selector: Selector? = nil,
    pseudo: Pseudo? = nil
  ) -> HTMLInlineStyle<Self> {
    self.inlineStyle(zIndex, media: media, selector: selector, pseudo: pseudo)
  }
}
