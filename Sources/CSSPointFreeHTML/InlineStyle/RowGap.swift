import CSSTypes
import PointFreeHTML

extension HTML {
    @discardableResult
    public func rowGap(
        _ rowGap: CSSPropertyTypes.RowGap?,
        media: CSSAtRuleTypes.Media? = nil,
        selector: Selector? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(rowGap, media: media, selector: selector, pseudo: pseudo)
    }
}
