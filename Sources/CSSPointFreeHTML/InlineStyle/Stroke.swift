import CSSTypes
import PointFreeHTML

extension HTML {
  @discardableResult
  public func stroke(
    _ stroke: CSSPropertyTypes.Stroke?,
    media: CSSAtRuleTypes.Media? = nil,
    selector: Selector? = nil,
    pseudo: Pseudo? = nil
  ) -> HTMLInlineStyle<Self> {
    self.inlineStyle(stroke, media: media, selector: selector, pseudo: pseudo)
  }
}
